						<h3>CONTENT ARCHITECTURE</h3>
						<h4>Content List/FAQ Answers</h4>
						<h5>Scenario 1: Can I look up your writings by genre?</h5>
						<ul>
							<li>A link to a page of genres.</li>
							<li>These genres will be links that take to separate pages of writings that fall under that genre.</li>
							<li>Brief text descriptions given about the genre.</li>
							<li>Number of writings under the particular genre.</li>
							<li>Clip art to match the genre.</li>
						</ul>
						<h5>Scenario 2: Will I be able to request the type of writing?</h5>
						<ul>
							<li>Genre pages will have writings categorized under short story/poems/etc.</li>
							<li>For requests there will be a Contact section to submit request to the writer.</li>
						</ul>
						<h5>Scenario 3: Will there by a forum to discuss the writings with other browsers that have read the samples?</h5>
						<ul>
							<li>The writings will appear on their own pages with comment sections for discussion and feedback.</li>
							<li>Users will type in a guest name and then their comment. Feedback is desired!</li>
						</ul>
						<h5>Scenario 4: Will the author of the writing samples reply to questions?</h5>
						<ul>
							<li>The site has its own email that will be monitored. You can send messages using the form under the
							'Contact' section! I will definitely respond!</li>
							<li>I will also participate in the comments.</li>
						</ul>
						<h5>Scenario 5: How often will the author post new writing samples?</h5>
						<ul>
							<li>As of now there won’t be set days or times for postings but that could change.</li>
							<li>There will be a news section on the site for these kinds of announcements or changes.</li>
						</ul>
						<h5>Scenario 6: Will there be alerts to the reader's email for new content?</h5>
						<ul>
							<li>Currently notifications/accounts are not a plan for the site.</li>
						</ul>
						<h5>Scenario 7: Can I save my favorite content as a favorite to come back to?</h5>
						<ul>
							<li>Currently no plan for accounts which would be how favorite content would work.</li>
							<li>Browser bookmarks and saved for offline reading will still be options through browser functionality.</li>
							<li>Capability to share will also be implemented!</li>
						</ul>
						<h5>Scenario 8: Will any content be available to download?</h5>
						<ul>
							<li>No content to download as the writing would be, to be viewed and read only.</li>
							<li>The page can be saved for offline reading through browser functionality.</li>
							<li>Capability to share will also be implemented!</li>
						</ul>
						<h5>Scenario 9: What other content is available besides writing?</h5>
						<ul>
							<li>This site is first and foremost a site for writing</li>
							<li>There will be a section for videos going over the writing process/brainstorming/’campfire story’ readings/etc.</li>
						</ul>
						<h5>Scenario 10: Are we able to learn anything about the writer?</h5>
						<ul>
							<li>The 'about' section will have a short text biography.</li>
							<li>There will be a photo with the biography.</li>
							<li>Links to social media will be included in the about section as well as at the bottom of each page.</li>
						</ul>
