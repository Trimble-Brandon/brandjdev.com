            <p class="intro postContent">
              The first decision to be made when planning to travel is,
              of course, the destination. I will begin by explaining why
              I chose Norway. It was a simple choice for me, but may not seem so to you.
            </p>

            <p class="postContent">
              Let’s reach back to when I was a kid and dad enjoyed some competitive gaming
              with coworkers. The game at the time was Age of Empires. Naturally, I was
              curious and wanted to play. When Age of Mythology was released, that was when
              the big impact came. I was captivated by the stories of the Greeks, the Egyptians,
              and the mysterious Norse. I found myself reading books on the Greek gods. I
              became fascinated with the stories. When the movie Troy was released I watched
              in awe as I saw this great war and the legends I had read about come to life
              before my eyes. I delved into the Egyptian mythology just the same. The Mummy,
              Scorpion King, and the Indiana Jones movies were some that I watched to feed
              my interest in these stories. I always found myself coming back to the mysterious
              Norse. Sadly there wasn’t as much material for me to find on them and so I had
              to make do with what I had. However, there were different stories about Dwarves
              that I would later learn paralleled the beliefs of the Norse.
            </p>

            <p class="postContent">
              Many years went by, I started and finished High School, left on a two year mission for
              my church, and returned. Now while on a mission we don’t watch movies. We dedicate the
              full two years to service to others. When returning and embracing my family for the first
              time in two years, it wasn’t long before we went to the movies. I don’t remember the
              movie we saw but I remember seeing the previews for the TV show Vikings’ second season.
              My interest was piqued! Finally a look into these warriors and culture that I had been
              fascinated with for so long. The show ultimately captivated me and due to the success of
              the show and the writer’s legitimacy among the historical community, old texts were allowed
              to be translated to English. I got more access to knowledge into my possession.
              <q>The Sacred Havamal</q>, <q>The Sagas of Ragnar Lodbrok and his Sons</q>,
              <q>The Poettic Edda</q> and <q>The Prose Edda</q>. I am currently reading and loving
              <q>American Gods</q> by Neil Gaiman, though I realize I am late to the party on American Gods.
            </p>

            <p class="postContent">
              It has been during this time of reveling in new access to historical texts and stories
              that, due to curiosity, I found my family history linking back to Norway, Sweden and
              Denmark on each side of my family. The furthest the line goes back is to the 700s with
              my family being descended through Siguror ‘Ormr i auga’ Ragnarssen.
            </p>

            <p class="postContent">
              This is why I want to go to Norway. I have the itch to travel and many countries are
              on my horizons but I feel the personal call to Norway. I am excited to float down the
              fjord, visit the museums, and stand at the tops of cliffs overlooking the valleys and
              fjords. Sadly, I’ll miss seeing the northern lights this year, unless I am very lucky.
            </p>

