              <img src="/images/norwegianHeader.jpg" alt="Norwegian Plane Image" width="570" height="356" />
              <h1 class="postTitle">From Longboats to Airlines</h1>
              <time class="postTime">12 August 2016</time>
            </div>

            <p class="intro postContent">
                A couple years back I was preparing to show my parents the country of England,
                where I had spent two years. My father, being the thrifty, cunning, and curious
                man that he is, discovered the airline that I now swear by. The difference in
                cost had me terrified that we would be flying in a rusty, duct-taped machine,
                which in my over-exaggerated imagination, could be aptly described as a
                screaming metal death trap.
            </p>

            <p class="postContent">
                I was pleasantly surprised and have preferred to fly with Norwegian Airlines
                ever since I stepped onto one of their planes. Yes the name is perfect. Ironic
                since I am presenting it to you as I write about a trip to Norway. Let’s take a
                look at the price difference using my flight to Norway as an example. I still
                like to price compare just because it is so impressive. You can try it out with
                many other major airlines, for this example I’ll use the one I originally compared
                with. Delta Airlines.
            </p>

            <p class="postContent">
                <strong>Delta Airlines -</strong> Leaving: 21 March 2017, Returning: 13 April 2017. Includes the
                one checked bag, carry-on, and meal since it is an international flight.
                <u>Price:</u> $1,262.00 as of 8/11/2016.<br />
                <br />
                Reference: <a id="deltaLink" target="_blank" href="https://www.google.com/flights/#search;f=SLC;t=OSL;d=2017-03-21;r=2017-04-13;a=DL;q=Delta+airlines">Delta</a>
            </p>

            <p class="postContent">
                <strong>Norwegian Airlines –</strong> Leaving: 21 March 2017, Returning: 13 April 2017. Only
                includes carry-on. Checked bags can be added. Meals can be added. There is a
                food cart and bar on the international plane. International plane also has
                wifi, personal entertainment system, very nice leather seats that are outfitted
                with USB plugs and outlets for your devices. Check their <a id="norwegianSiteLink" target="_blank" href="http://www.norwegian.com/us/travel-info/">site</a> for more info.
                <u>Price:</u> $572.80 as of 8/11/2016.<br />
                <br />
                Reference: <a id="norwegianLink" target="_blank" href="http://www.norwegian.com/us/booking/flight-tickets/farecalendar/?D_City=LAS&A_City=OSL&D_SelectedDay=23&D_Day=23&D_Month=201703&R_SelectedDay=10&R_Day=10&R_Month=201704&CurrencyCode=USD&TripType=2">Norwegian</a>
            </p>

            <p class="postContent">
                Humor me as I do some simple math here. $1,262.00 – $572.80 = <strong>$689.20</strong>.
            </p>

            <p class="postContent">
                You can pocket that, or maybe see another country, no big deal. The price will
                go up if you pack heavily and need constant refreshments, but you will have to
                try, to reach other airlines’ pricing. I have been on three international
                flights with Norwegian Airlines. Three weeks is generally how long I stay on a
                trip. I only pack a carry-on and don’t purchase refreshments. The other catch
                is that Norwegian is not at every airport, so you will have to plan accordingly.
                I have to drive three hours to the nearest airport but for the price it is more
                than worth it.
            </p>

            <p class="postContent">
                Packing is a big deal, especially if you want to avoid checking multiple bags and
                paying extra to do so. I talk about how I pack in the next
                <a id="packingLink" href="/writing/packing.php">post</a>.
            </p>

            <p class="postContent">
                I hear a lot of people complain about not having the money to travel. I know
                that this information about this airline will open up more possibilities. Try
                out different flights on their site and compare. See for yourself. I know
                circumstances vary. Family, work, other responsibilities, but I would like,
                from a financial focus, put in perspective for you;<br />
                <br />
                Imagine you are someone who gets a daily coffee every morning. Let’s say for
                the sake of this example that the coffee costs <strong class="blueText">$5.00</strong>.
                There are <strong class="redText">three hundred and sixty-five</strong> days in a year.
                Simple math: <strong class="redText">365</strong> * <strong class="blueText">5</strong> = <strong>$1,825</strong>.
                If you got a coffee every other day you would save $912.50 and still have
                enough to travel. Or you can start taking a water and start seeing the world.
            </p>

