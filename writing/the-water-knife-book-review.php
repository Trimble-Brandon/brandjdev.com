              <h1 class="postTitle">TWK Book Review</h1>
              <time class="postTime">9 July 2016</time>
            </div>
            <img class="textWrapImg" src="../images/twkbr.jpg" align="left" alt="The Water Knife by Paolo Bacigalupi" width="318" height="474" />

            <p class="intro postContent">
              With school I spend a lot of time reading textbooks and so I
              started looking for new ideas on works of fiction to read in spare
              time. I am making sure to get some more fiction read. I read
              analytically and so coupled with schoolwork I finish books at my
              own pace. With that being said, Reddit introduced
              <q>The Water Knife</q> by Paolo Bacigalupi. The summary of this
              book is what initially caught my attention:
            </p>

        	  <p class="postContent"><q><i><strong>
              WATER IS POWER –</strong> In the near future, the Colorado River
              has dwindled to a trickle. Detective, assassin, and spy Angel
              Velasquez <q>cuts</q> water for the Southern Nevada Water
              Authority, ensuring that its lush arcology developments can bloom
              in Las Vegas. When rumors of a game-changing water source surface
              in Phoenix, Angel is sent south to hunt for answers that seem to
              evaporate as the heat index soars and the landscape becomes more
              and more oppressive. There, he encounters Lucy Monroe, a hardened
              journalist with her own agenda, and Maria Villarosa, a young Texas
              migrant, who dreams of escaping north. As bodies begin to pile up,
              the three find themselves cast as pawns in a game far bigger and
              more corrupt than they could have imagined. When water is more
              valuable than gold, alliances shift like sand and the only truth
              in the desert is that someone will have to bleed if anyone hopes
              to drink.</i></q>
            </p>

        	  <p class="postContent">
              Post-apocalyptic type worlds catch my attention and the idea of
              such a world looming in a not so distant future is both alluring
              and captivating. Bacigalupi has a clear message in this book. I
              found myself appreciatively drinking more water and feeling
              ashamed whenever I needed the water to run so that it would heat
              up for dishes or for the shower. We are looking at a corrupt,
              dusty, struggling world in this book. The atmosphere brings to
              mind a mixture of <q>The Book of Eli</q> and <q>Interstellar.</q>
            </p>

            <p class="postContent">
              The story is told from three perspectives mentioned in the
              summary. Angel, Lucy, and Maria. Angel and Lucy are old enough to
              have experienced the <q>old world</q> or our world but we don’t
              get an exact <q>Lucy is such and such years old.</q> I got the
              impression that they were mid to late thirties while Maria is said
              to be a teenager.
            </p>

        	  <p class="postContent">
              <strong>Angel –</strong> The first perspective of the world that
              we get is from Angel. I was worried at first that Angel was going
              to just be the typical hardcore, destroy everything, BA
              character, but he has a very interesting character development
              and there are some gems with him. I had to reread these gems in
              particular because there is a lot of imagery, understanding
              of religion and Mexican culture.
            </p>

        	  <p class="postContent">
              <strong>Lucy –</strong> We meet Lucy next and I found Lucy to be
              the character that I related with the most. She is a Journalist in
              the city of Phoenix and she finds herself away from her family and
              struggling to make this city a better place. The media represents
              a lot of the media that we see today. They are pushing for an
              agenda and report news that supports that agenda and keeps people
              in the dark. Ironic. Lucy is truthful and has gotten her in
              trouble in the past and has gotten people who have protected her
              in danger. We meet her at a time where she has been beat down by
              this world but still unwilling to give up and is holding on to
              hope.
            </p>

        	  <p class="postContent">
              <strong>Maria –</strong> We meet Maria with her friend Sarah, both being Texan refugees whose families are now dead.
              Maria is Ambitious but also angry with less hope in the world. She never saw the <q>old world</q> and many people believe that world will
              come back but that idea upsets her because of what she has experienced.
            </p>

        	  <p class="postContent">
              The author has two major writing techniques that really stood out for me that I appreciated and enjoyed.
            </p>

        	  <p class="postContent">
              <strong>The first:</strong> The author doesn’t spell everything out for you. A lot of the world we are learning about
              requires extra information that is not in the book. You need at least an understanding or appreciation of religion to understand the group
              called <q>Merry Perrys.</q> Google the Spanish words that you come across. Many are just profanities, but even then, knowing adds context to the
              conversations. There is an advance and twisting of our pop culture. You will also need general political knowledge to appreciate the themes
              that are being represented, but these are easily gained by just paying attention to what is happening in the world.
            </p>

        	  <p class="postContent">
              <strong>The second:</strong> I deeply enjoyed the dialogue. Dialogue is a staple for learning about the characters and I
              feel Bacigalupi did very well. This is in particular with Angel. He doesn’t crack often but when he does you learn a lot and
              much is from images you get from his mind. Don’t move on without understanding his dreams/visions/thoughts. They are fantastic moments.
            </p>

            <p class="postContent">
              I do have to warn you, there are two sexual encounters in this book. The first is a threesome and is
              not overly descriptive but you understand what is going on. The second, now I have never read an erotic novel, but
              the second scene goes far more into detail. Both are necessary to the story and development of the characters
              involved. They don’t feel tossed in for the sake of just being there.
            </p>

            <p class="postContent">
              I found one glaring plothole with the climax of the story. It is just an anoying detail. The climax
              was different in terms of the usual books that I read. Don’t get me wrong it ends great, there
              is just one detail that is mentioned earlier that could ruin the ending and if it had been
              addressed would have been fine so it just bothers me.
            </p>

        	  <p class="postContent">
              Overall, this book is dark. It is gritty. It is a look at a hard potential
              future. It has beautiful elements that are to be appreciated within an interesting world.
              The theme is a warning and echoes danger to the current world we live in.
            </p>

