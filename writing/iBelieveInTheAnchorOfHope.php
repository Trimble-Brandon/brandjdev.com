		        <p class="intro postContent">Without an anchor a boat will drift. Without strong and deep roots in the midst of a storm,
                    a tree will fall. Uneasy footing will end up with any person falling and feeling a bit foolish
                    afterwards. Without an anchor, a steady assurance, we will lose ourselves. I believe in the
                    Anchor of Hope. For, it is hope that gives us unwavering strength in the face of the onslaught
                    of wind and fury of storm.</p>

                    <p class="postContent">During my time as a Missionary in England, I would learn and reflect often on what hope means to me and what
                    effect it has on all people. This happened so often because of the baffling responses I would receive from
                    those who weren’t interested in God. Much more difficult for me to understand were the angry responses of
                    <q>What has He ever done for me? Why should I give Him my time when He has…</q> fill in the blank. I came to
                    the conclusion that Hope is the center of why we believe in anything, why faith is even held among any sort of
                    person.</p>

                    <p class="postContent">I remember a specific experience I had where this troubled me the most deeply. I was serving in the Capital
                    city of Wales. Those who know the UK know that it rains. It rains always. Wales was of no exception. So much so
                    it felt sometimes as though I were sputtering beneath the torrents of Niagara Falls. The Memory I recall was one
                    of those days. I was biking with my companion when we stopped and locked up our bikes near a bus stop. It was here
                    that we were meeting the Bishop and another Priesthood member to go and visit other members of the congregation.
                    They were not there and I noticed someone curled up into the corner of the bus stop. We walked over to talk to her
                    and I halted as we came close. She had a worn, ragged stroller with a young baby asleep. It was apparent she was far
                    along with her second. She wore a ratty pair of leggings, a ripped shirt and a tattered soaked jacket. Lines of
                    abuse, mental, physical and emotional were deep and profound in her face. Her hair seemed wispy and unkempt. It was
                    wet, but obvious it had not been cleaned properly in some time. She peered up to us through dark eyes.</p>

                    <p class="postContent"><q>Don’t bother.</q> She whispered piercingly. <q>I know who you are. There is no hope for me. So, you may as well
                    move on to someone more worth your time.</q> Her voice shook and broke when she spoke. She looked away. Her eyes
                    squinting up and her lips pursed. I looked at my companion and then we sat down next to her. We went on to give our
                    say. Of how she was completely worth our time like any other. That God loved her and that whatever she was going
                    through He could give her a way to heal. She then stopped us to tell us her story. She was young when her father beat
                    her, and her mother, then ultimately abandoned her. How she foolishly sought him in all her relationships. She submitted
                    to the wills of abusive men who took what they wanted from her. She turned to drugs, hoping that if she did so she would
                    be loved or feel something that she never felt she’d had. She then continued to say how she loved her Savior. She said
                    <q>I have caused Him enough pain already. The knowledge that He suffered for my sins destroys me. I am lost and if I
                    stay away and there is some small chance His suffering may be lightened, then it is better that way.</q> We tried
                    so hard to explain and to testify. She had been robbed of hope. Without it, she disappeared into the rain of
                    that dark evening.</p>

                    <p class="postContent">I then learned and felt a deep urgency to help bring Hope to others. It is hope that is slowly seeping from the hearts
                    of man. Without it our faith is weak and our love is dulled. I have seen how it is hope that keeps people fighting
                    and gives them a strength that truly defines us as human beings. Without that anchor we are uprooted in the relentless
                    onslaught of the storms we will face throughout life.</p>

