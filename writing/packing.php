              <h1 class="postTitle">Packing: Simplified For Efficient Travel</h1>
              <time class="postTime">22 August 2016</time>
            </div>

            <p class="intro postContent">
                An obstacle with all trips is packing. Since I am flying with Norwegian Airlines,
                in order to keep the great cost you must avoid the extra expenses. A carry-on is
                included with the basic ticket. Any additional luggage that is stored below the
                plane is billed. In the past I have taken a backpack and a small suitcase which
                were allowed as my carry-on. This plan changed as I continued to consider my
                options. I will not have consistent places I will be staying, so there is
                nowhere for me to leave my stuff. I will be hiking and spending a lot of
                time on foot, so a single backpack is ideal. I fit all accessories, clothing etc.
                into my single backpack.
            </p>

            <img class="centeredPostImg" src="/images/DSCN5832.JPG" alt="Packing Items Image" width="700" height="525" />

            <p class="postContent">
                Let’s take a look at the non-clothing items first:
            </p>

            <ul class="postContent">
                <li>
                    Trimmer/shaver
                </li>

                <li>
                    Hair Brush
                </li>

                <li>
                    Toothbrush and Toothpaste
                </li>

                <li>
                    Deodorant
                </li>

                <li>
                    Wallet
                </li>

                <li>
                    Notebook(the paper kind)/Pens
                </li>

                <li>
                    Surface Pro 4/Charger and USB hub
                </li>

                <li>
                    Phone
                </li>

                <li>
                    Camera/Charger and USB cord
                </li>

                <li>
                    International Power Converter
                </li>

                <li>
                    2DS/Charger
                </li>
            </ul>

            <p class="postContent">
                <strong>Trimmer/shaver:</strong> This is not absolutely necessary.
                I maintain a short beard and I have very fast growing facial hair.
                I could leave it home, save the space and weight, and just treat
                Norway as my personal Jumanji and return looking the part, but
                I decided that I prefer to keep the neck beard banished.<br />
                <br />
                <strong>Hair Brush:</strong> You could forego the brush easy enough,
                but I, fingers crossed, am not cast away and want to be presentable.<br />
                <br />
                <strong>Toothbrush and Toothpaste:</strong> I am bringing these just
                so I don’t have to buy them when I get to Norway. I have two small
                <q>security appropriate</q> containers of toothpaste with my
                toothbrush wrapped in a plastic bag.<br />
                <br />
                <strong>Deodorant:</strong> You can smell, if you want, but I don’t
                want to be that guy everyone hates on the airplane.<br />
                <br />
                <strong>Wallet:</strong> Money of course! Be sure to let your bank
                know ahead of time that you are traveling. Nothing is worse than
                your card being declined when you are on the other side of the world.<br />
                <br />
                <strong>Notebook/Pens:</strong> You never know when you need to jot
                something down. Whether inspiration strikes to write, draw, pass on
                a note, or record something someone says. Don’t take what you can
                experience at any time on any day for granted.<br />
                <br />
                <strong>Surface Pro 4:</strong> As much as I would like to be fully
                off grid, while lost in the adventure, my family would not appreciate
                it, I would fail my math class, and I watched 127 hours. Learn from
                other’s mistakes. Yeah, I am taking my math class for school over
                summer. The plan is currently to have next week done by tomorrow. The
                next week done on the Sunday etc. It’s not ideal, but I will make it work.<br />
                <br />
                <strong>Phone:</strong> Source of contact when I stop by areas that
                have Wi-Fi. I also have maps of Norway downloaded, music, and the
                phone will be a backup camera.<br />
                <strong>Camera:</strong> I have a Nikon Coolpix AW100 that I have had
                for several years now. It tracks where you took pictures to GPS and
                takes very good high quality 16MP pictures. It also records 1080p
                video. It is a durable camera meant for outdoors. The features and
                great quality make it a must have for this trip.<br />
                <br />
                <strong>International Power Converter:</strong> This is probably the
                only item on this list that may turn heads. This item has plugs for
                US/AUS/UK/EU. The plug extends and goes into the country’s port. The
                front has a universal port that you then plug your own devices into.
                This will be used for my camera’s battery charger, my surface charger,
                and the charger for my trimmer.<br />
                <br />
                <strong>2DS:</strong> I’m a geek and I need entertainment while at the
                airport and on the plane. I have tried reading before and it doesn’t
                work for me. At the airport I get too distracted by everything and I
                talk to people. Playing a game is much easier for me to stop and
                continue. Interruptions while reading is aggravating too me. On
                international planes, the ambience is so loud I have a hard time
                sleeping. The planes have entertainment systems stocked with movies,
                but if I don’t find something I am in the mood for, then I am out of
                luck. 2DS is portable and easy entertainment for me when I need to pass
                the time waiting.
            </p>

            <img class="centeredPostImg" src="/images/DSCN5834.JPG" alt="Packing Items Image" width="700" height="525" />

            <p class="postContent">
                This is a vacuum sealed bag. If you have never seen one, you put clothes
                in it, seal it shut and use your vacuum on the nozzle to compress the bag
                and remove air. This saves an impressive amount of space! I first started
                using these for packing while I was in England on my mission. I seriously
                got four for a pound at Poundland and they lasted several years. I got this
                one at Walmart. They come in packs so you have to hunt but I did find a
                single bag. It was only around five dollars. I will be bouncing around to
                places and when I sleep at a place, it will be Airbnb. So when doing this
                packing, make sure you are going to a place with some form of vacuums
                available. Homes have vacuums and when I am not staying where there is a
                vacuum, I will find one at a gas station. In the cases where I absolutely
                cannot use a vacuum. I have a small enough amount of clothes that they do
                fit without being air compressed.
            </p>

            <img class="centeredPostImg" src="/images/DSCN5836.JPG" alt="Packing Items Image" width="700" height="525" />

            <p class="postContent">
                These are all the clothes I am bringing.
            </p>

                <ul class="postContent">
                    <li>
                        Three shirts packed. One worn. I decided I am going to buy
                        some shirts in Norway. So save the room.
                    </li>

                    <li>
                        Two pairs of jeans packed. I am wearing ripstop cargo pants
                        for the flight and will have them if they are needed during
                        the rest of the trip. They are the practicality.
                    </li>

                    <li>
                        Seven pairs of socks.
                    </li>

                    <li>
                        Seven pairs of underwear.
                    </li>

                    <li>
                        One pair of shorts.
                    </li>

                    <li>
                        One under armor shirt.
                    </li>

                    <li>
                        One belt.
                    </li>

                    <li>
                        One pair of boots.
                    </li>
                </ul>

            <p class="postContent">
                That’s it. I plan on doing laundry once a week. Shorts are for
                swimming. Shoes are a hassle to bring multiple pairs. I only really
                need boots. For the shorts, if I need shoes, I’ll get some cheap
                flip flops.
            </p>

            <img class="centeredPostImg" src="/images/DSCN5837.JPG" alt="Packing Items Image" width="700" height="525" />

            <p class="postContent">
                Pack it in the sealing bag and seal it. I packed the clothes to
                fill half the bag then folded it. I also folded the top and bottom
                to condense it more. Depending on the size of your bag, will cause
                how creative you need to be. After folding, use the vacuum to
                compress everything.
            </p>

            <img class="centeredPostImg" src="/images/DSCN5838.JPG" alt="Packing Items Image" width="700" height="525" />

