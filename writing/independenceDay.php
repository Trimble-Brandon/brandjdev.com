                    movie at least every Fourth of July. It is a tradition for me among others that I enjoy on this
                    day. What has always stood out to me in the movie is humanity uniting together against a
                    threatening enemy. Why is it that we need a greater threat to lay our differences aside and unite?</p>

                    <p class="postContent">I have ancestors that came from Ireland, Scotland, Wales and England. I spent two years in
