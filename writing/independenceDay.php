                    cheeky of me to recognize that Britain’s exit from the European Union has taken place so close to
                    our Independence Day. I want the people of England to know that although we zealously celebrate our
                    independence as a nation, that it does not undermine our love and respect for you. We stand with
                    you. Independence as a country does not mean you are alone.</p>

