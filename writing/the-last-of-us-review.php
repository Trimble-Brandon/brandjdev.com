              <p class="postContent">
                The Last of Us takes place in a post-apocalyptic world where, rather than the usual zombie
                experience, the epidemic comes from a real infection strain called Cordyceps. Cordyceps
                in our real world is specific to only insects, though in the game the infection obviously
                has mutated and infects humans. This infection grows in the body and takes over the brain
                causing increased agitation, extreme muscle spasms, bloating and fungal growth. Eventually
                when the body is dead the fungal growth extends out of the body reaching for light and
                emitting spores to spread the infection.
              </p>

              <p class="postContent">
                This adds an interesting dynamic to the story and a much needed change in the Zombie Genre.
                The Last of Us cannot be considered simply a zombie story, so donâ€™t roll your eyes at the
