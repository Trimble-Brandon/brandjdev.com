              </p>

              <p class="postContent">
                The story of The Last of Us is rooted in the characters. Primarily the main two. This is the
                story of Joel and Ellie. This is a dark game in tone and heavy in emotion, with
                Creative Director and writer, Neil Druckmann, working hard to present the realism that this
                sort of world would bring. This story, despite having traumatic events, has beautiful moments
                that will have you beaming and unashamedly in tears. It is a story about humanity, strength,
                survival, relationships, determination, tragedy and love.
              </p>

