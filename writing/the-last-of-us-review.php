            <section id="vgAsArt">
              <h2>Video Games Setting the Standard For Interactive Storytelling</h2>
              <p class="intro postContent">
                I consider myself an avid consumer of creativity. I love books, movies, paintings,
                music, video games and everything between. To me each of these are based on stories.
                There is an experience and story behind every work of art that brings it to life and
                gives it meaning. This causes a resonance in people that differs from person to
                person. Each person connects differently and either cares for the story and finds
                meaning or doesn't connect with it.
              </p>

              <p class="postContent">
                Let's take a look at a few different forms of art and the points that support them
                being considered art. We'll start with the earliest/basic forms. I am not going to
                go hugely in-depth, just introduce and cover some basic concepts.
              </p>

              <section id="paintings_drawings">
                <h3>Paintings/Drawings</h3>

                <p class="postContent">
                  Paintings, drawings etc. are visually stimulating. Color, shapes, light, characters,
                  scenes etc. are used to tell a story. The artist generally has that story in their
                  mind when they are creating, however the interpretation is often left up to the
                  viewer.
                </p>
              </section>

              <section id="music">
                <h3>Music</h3>

                <p class="postContent">
                  Stimulated through listening, music is art that is very emotionally stirring.
                  Music is also a story being told that is very capable at setting the mood
                  of a listener.
                </p>
              </section>

              <section id="writing">
                <h3>Writing</h3>

                <p class="postContent">
                  Writing is interesting because it is an accumulated art form. A person reads
                  and forms what is presented in their minds. The art is entirely made piece by
                  piece. Writing allows the writer to control information or the pieces a person
                  receives by the piece, building up and creating a full picture.
                </p>
              </section>

              <p class="postContent">
                These would be the early basic forms of art. <strong>Photography</strong> has to be noted as a branch
                of painting/drawing coming about with the invention of cameras. The capability to
                capture a moment in time that uses the same visual styles of paintings and drawings.
                This leads to the cumulative art form of Film, Movies and Television. Whatever you
                want to call it, the idea is the same.
              </p>

              <section id="theatre">
                <h3>Theatre</h3>

                <p class="postContent">
                  The raw combining of music and visuals. Actors are also introduced as forms of art
                  where a person portrays the emotions of a character. A play has the advantage of
                  being live and in the moment which gives a special characteristic and sets itself apart.
                </p>
              </section>

              <section id="film">
                <h3>Film</h3>

                <p class="postContent">
                  Story that is written as a blueprint and told through dialogue, visuals and music. Film
                  has the beautiful capability of presenting all of these together to create a full experience.
                  Building on the experience that Theatre introduced. Film gives powerful control over how a
                  person is seeing the story through cinematography.
                </p>
              </section>

              <section id="videogames">
                <h3>Video Games</h3>

                <p class="postContent">
                  This brings us into Video Games. Like all the arts mentioned previously, there are many different
                  forms that can be appreciated for their different characteristics. I want to focus on story
                  driven Video Games as my example.
                </p>

                <p class="postContent">
                  The primary aspect that sets Video Games apart is the interactivity of the story. You control
                  a character or characters and make choices and help them progress through the story. Video
                  Games have the benefit of visual stimulation through cinematography, stimulation through
                  listening with music and sound design, emotional portrayal from actors with the rapidly
                  evolving capabilities of motion capture, and the playerâ€™s having the chance to have an active
                  part in the story. This brings us to my chosen example of the massive award winning
                  <a href="http://www.thelastofus.playstation.com/index.html" target="_blank">The Last of Us</a> created
                  by Naughty Dog with <a href="https://en.wikipedia.org/wiki/Neil_Druckmann" target="_blank">Neil Druckmann</a> as the
                  creative director and writer.
                </p>
              </section>

            </section>

